            print(f"üìã –¢–∞–±–ª–∏—Ü—ã –≤ –ë–î: {tables}")

            # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å–æ–¥–µ—Ä–∂–∏–º–æ–µ —Ç–∞–±–ª–∏—Ü
